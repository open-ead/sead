template <typename T>
T MathCalcCommon<T>::gcd(T x, T y)
{
    if (x == 0 || y == 0)
        return 0;

    while (x != y)
    {
        if (x > y)
            x -= y;
        else
            y -= x;
    }

    return x;
}

template <typename T>
T MathCalcCommon<T>::lcm(T x, T y)
{
    if (x == 0 || y == 0)
        return 0;
    return x / gcd(x, y) * y;
}

